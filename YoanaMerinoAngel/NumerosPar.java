public class NumerosPar {  //Clase principal
    public static void main(String[] args) { //Metodo principal
        for (int i = 2; i <= 1000; i += 2) {  // Bucle que inicia en 2 y avanza de 2 en 2 hasta 1000
            System.out.println(i); // Imprime el número actual (siempre par)
        } // Cierre del bloque "for"
    } // Cierre del método "main"
} // Cierre de la clase "NumerosPar"
