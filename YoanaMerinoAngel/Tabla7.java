public class Tabla7 { //Inicio de la clase
    public static void main(String[] args) { // Método principal donde se ejecuta el programa
        int numero = 7; // Número base de la tabla
        for (int i = 1; i <= 10; i++) { // Bucle del 1 al 10
            System.out.println(numero + " x " + i + " = " + (numero * i)); // Imprime la multiplicación
        } // Cierre del bloque "for"
    } // Cierre del método "main"
} // Cierre de la clase "Tabla7"
